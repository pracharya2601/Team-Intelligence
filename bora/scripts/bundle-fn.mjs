/**
 * Bundle a Butterbase function + its local ./_shared imports into ONE self-contained file.
 *
 *   node scripts/bundle-fn.mjs functions/recall-webhook.ts
 *   → writes functions/_deploy/recall-webhook.ts  (inlined, no relative imports)
 *
 * Why: Butterbase deploys a function as a single code blob — relative `./_shared/*` imports
 * don't resolve at runtime. This inlines them. Source files keep their clean imports for the repo;
 * we only deploy the bundled output.
 *
 * Strategy: resolve the _shared dependency graph TRANSITIVELY (a _shared file may import another,
 * e.g. escalate.ts → llm.ts), topologically order it, strip module syntax, and prepend. Good
 * because our _shared files are self-contained (native fetch + Web Crypto, no external deps).
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const entry = process.argv[2];
if (!entry) {
  console.error("usage: node scripts/bundle-fn.mjs <functions/file.ts>");
  process.exit(1);
}

// Matches `import ... from "./_shared/X.ts"` (entry) or `import ... from "./X.ts"` (within _shared).
const IMPORT_SHARED_ENTRY = /import\s+(?:type\s+)?(?:\{[^}]*\}|\*\s+as\s+\w+|\w+)\s+from\s+["']\.\/_shared\/([\w.-]+?)(?:\.ts)?["'];?/g;
const IMPORT_REL = /import\s+(?:type\s+)?(?:\{[^}]*\}|\*\s+as\s+\w+|\w+)\s+from\s+["']\.\/([\w.-]+?)(?:\.ts)?["'];?/g;

function sharedDepsOf(src, fromEntry) {
  const names = new Set();
  const re = fromEntry ? IMPORT_SHARED_ENTRY : IMPORT_REL;
  for (const m of src.matchAll(re)) names.add(m[1].replace(/\.ts$/, ""));
  return names;
}

function stripModuleSyntax(src) {
  return src
    .replace(/^\s*import\s+.*?from\s+["'][^"']+["'];?\s*$/gm, "")
    .replace(/^\s*import\s+["'][^"']+["'];?\s*$/gm, "")
    .replace(/^\s*export\s+(default\s+)?/gm, "");
}

const entryPath = join(root, entry);
const entryCode = readFileSync(entryPath, "utf8");

// Walk the _shared graph depth-first → topological order (deps before dependents).
const ordered = [];
const seen = new Set();
function visit(name) {
  if (seen.has(name)) return;
  seen.add(name);
  const p = join(root, "functions", "_shared", `${name}.ts`);
  const src = readFileSync(p, "utf8");
  for (const dep of sharedDepsOf(src, false)) visit(dep); // transitive deps first
  ordered.push({ name, src });
}
for (const name of sharedDepsOf(entryCode, true)) visit(name);

const preamble = [];
for (const { name, src } of ordered) {
  preamble.push(`// ─── inlined from _shared/${name}.ts ───`);
  preamble.push(stripModuleSyntax(src).trim());
}

// Remove the shared-import lines from the entry; keep the rest (incl. its default export).
const code = entryCode.replace(IMPORT_SHARED_ENTRY, "").trimStart();

const banner = `// AUTO-GENERATED by scripts/bundle-fn.mjs from ${entry} — DO NOT EDIT. Edit the source instead.\n`;
const out = banner + preamble.join("\n\n") + "\n\n// ─── entry: " + entry + " ───\n" + code;

const outDir = join(root, "functions", "_deploy");
mkdirSync(outDir, { recursive: true });
const outPath = join(outDir, entry.split("/").pop());
writeFileSync(outPath, out);
console.log(`✓ bundled ${entry} → functions/_deploy/${entry.split("/").pop()} (${ordered.length} shared inlined: ${ordered.map((o) => o.name).join(", ") || "none"})`);
