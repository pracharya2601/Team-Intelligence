/**
 * Bundle a Butterbase function + its local ./_shared imports into ONE self-contained file.
 *
 *   node scripts/bundle-fn.mjs functions/recall-webhook.ts
 *   → writes functions/_deploy/recall-webhook.ts  (inlined, no relative imports)
 *
 * Why: Butterbase deploys a function as a single code blob — relative `./_shared/*` imports
 * don't resolve at runtime. This inlines them. Source files keep their clean imports for the repo;
 * we only deploy the bundled output.
 *
 * Strategy (simple, sufficient for our functions): for each `import { ... } from "./_shared/X.ts"`,
 * read that file, strip its own imports + `export` keywords, and prepend it. Good enough because
 * our _shared files are self-contained (native fetch + Web Crypto, no external deps).
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const entry = process.argv[2];
if (!entry) {
  console.error("usage: node scripts/bundle-fn.mjs <functions/file.ts>");
  process.exit(1);
}

const IMPORT_SHARED = /^\s*import\s+(?:type\s+)?\{[^}]*\}\s+from\s+["']\.\/_shared\/([\w.-]+?)(?:\.ts)?["'];?\s*$/gm;

function stripModuleSyntax(src) {
  return src
    // drop import lines (we inline everything into one scope)
    .replace(/^\s*import\s+.*?from\s+["'][^"']+["'];?\s*$/gm, "")
    .replace(/^\s*import\s+["'][^"']+["'];?\s*$/gm, "")
    // turn `export function/const/interface/type/class` into plain declarations
    .replace(/^\s*export\s+(default\s+)?/gm, "");
}

const entryPath = join(root, entry);
let code = readFileSync(entryPath, "utf8");

// Collect shared deps referenced by the entry.
const shared = new Set();
for (const m of code.matchAll(IMPORT_SHARED)) shared.add(m[1].replace(/\.ts$/, ""));

const preamble = [];
for (const name of shared) {
  const p = join(root, "functions", "_shared", `${name}.ts`);
  preamble.push(`// ─── inlined from _shared/${name}.ts ───`);
  preamble.push(stripModuleSyntax(readFileSync(p, "utf8")).trim());
}

// Remove the shared-import lines from the entry, keep the rest; the entry's default export stays.
code = code.replace(IMPORT_SHARED, "").trimStart();

const banner = `// AUTO-GENERATED by scripts/bundle-fn.mjs from ${entry} — DO NOT EDIT. Edit the source instead.\n`;
const out = banner + preamble.join("\n\n") + "\n\n// ─── entry: " + entry + " ───\n" + code;

const outDir = join(root, "functions", "_deploy");
mkdirSync(outDir, { recursive: true });
const outPath = join(outDir, entry.split("/").pop());
writeFileSync(outPath, out);
console.log(`✓ bundled ${entry} → functions/_deploy/${entry.split("/").pop()} (${shared.size} shared inlined: ${[...shared].join(", ") || "none"})`);
